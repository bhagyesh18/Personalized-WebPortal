package grid;

public class addLink {
	public String link_name;
	public String link_url;
	public String link_description;
	public String link_page;
	public String link_position;
	public String link_status;
	public String getLink_name() {
		return link_name;
	}
	public void setLink_name(String link_name) {
		this.link_name = link_name;
	}
	public String getLink_url() {
		return link_url;
	}
	public void setLink_url(String link_url) {
		this.link_url = link_url;
	}
	public String getLink_description() {
		return link_description;
	}
	public void setLink_description(String link_description) {
		this.link_description = link_description;
	}
	public String getLink_page() {
		return link_page;
	}
	public void setLink_page(String link_page) {
		this.link_page = link_page;
	}
	public String getLink_position() {
		return link_position;
	}
	public void setLink_position(String link_position) {
		this.link_position = link_position;
	}
	public String getLink_status() {
		return link_status;
	}
	public void setLink_status(String link_status) {
		this.link_status = link_status;
	}
	
}
